 public void serch(){
        
        String serch=sbox.getText();
        
        try {
            
            String sql="SELECT * FROM store WHERE name LIKE '%"+serch+"%' OR brand LIKE '%"+serch+"%' OR count LIKE '%"+serch+"%' OR id LIKE '%"+serch+"%' OR price LIKE '%"+serch+"%'  ";
            pat=conn.prepareStatement(sql);
            rs=pat.executeQuery();
            Table1.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
 
